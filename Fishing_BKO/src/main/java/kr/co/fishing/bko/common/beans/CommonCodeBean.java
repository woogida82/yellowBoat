package kr.co.fishing.bko.common.beans;


public class CommonCodeBean {
    
    private String masterCd;
	private String masterNm;
	private String commonCd;
	private String commonNm;
	private String dispNo;
	private String useYn;
	
	public String getMasterCd() {
		return masterCd;
	}
	public void setMasterCd(String masterCd) {
		this.masterCd = masterCd;
	}
	public String getMasterNm() {
		return masterNm;
	}
	public void setMasterNm(String masterNm) {
		this.masterNm = masterNm;
	}
	public String getCommonCd() {
		return commonCd;
	}
	public void setCommonCd(String commonCd) {
		this.commonCd = commonCd;
	}
	public String getCommonNm() {
		return commonNm;
	}
	public void setCommonNm(String commonNm) {
		this.commonNm = commonNm;
	}
	public String getDispNo() {
		return dispNo;
	}
	public void setDispNo(String dispNo) {
		this.dispNo = dispNo;
	}
	public String getUseYn() {
		return useYn;
	}
	public void setUseYn(String useYn) {
		this.useYn = useYn;
	}
}
